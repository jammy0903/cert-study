"""TOPCIT 02~06 챕터 재구조화"""
import json
from pathlib import Path

BASE = Path(__file__).parent / "topcit_json_v2"

def load(sid):
    with open(BASE / f"topcit_{sid}.json", encoding="utf-8") as f:
        return json.load(f)

def save(sid, data):
    with open(BASE / f"topcit_{sid}.json", "w", encoding="utf-8") as f:
        json.dump(data, f, ensure_ascii=False, indent=2)

def flat_secs(data):
    """모든 섹션을 제목→섹션 dict로"""
    seen = {}
    for ch in data["chapters"]:
        for sec in ch["sections"]:
            if sec["title"] not in seen:
                seen[sec["title"]] = sec
    return seen

def build(data, new_chapters):
    secs = flat_secs(data)
    result = []
    for ch_title, sec_titles in new_chapters:
        ch_secs = []
        for t in sec_titles:
            if t in secs:
                ch_secs.append(secs[t])
            else:
                print(f"  [경고] '{t}' 없음")
        if ch_secs:
            result.append({"title": ch_title, "sections": ch_secs})
    data["chapters"] = result
    return data

def stats(data):
    for ch in data["chapters"]:
        n = sum(len(s["concepts"]) for s in ch["sections"])
        print(f"  [{ch['title']}] {len(ch['sections'])}섹션 {n}개념")

