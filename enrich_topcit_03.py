def add_source_once(con, source):
    existing = con.setdefault("sources", [])
    for s in existing:
        if s.get("title") == source["title"]:
            return
    existing.append(source)


def main():
    with open(PATH, encoding="utf-8") as f:
        data = json.load(f)

    updated, missed = [], []
    targets = dict(ENRICHMENTS)

    for ch in data["chapters"]:
        for sec in ch["sections"]:
            for con in sec["concepts"]:
                title = con.get("title", "")
                if title in targets:
                    con["types"] = targets[title]
                    add_source_once(con, SOURCE)
                    updated.append(title)
                    targets.pop(title, None)

    missed = list(targets.keys())

    with open(PATH, "w", encoding="utf-8") as f:
        json.dump(data, f, ensure_ascii=False, indent=2)

    print(f"✓ enriched ({len(updated)}):")
    for t in updated: print(f"   - {t}")
    if missed:
        print(f"\n✗ NOT FOUND ({len(missed)}):")
        for t in missed: print(f"   - {t}")


if __name__ == "__main__":
    main()
